    $("button.do_submit").click(function(e){
     e.preventDefault();
     let form = $('#form_cs_contact')[0];
     let data = new FormData(form);
    
      $.ajax({
        url: "{{ route('processContact') }}",
        type: "POST",
        data : data,
        dataType:"JSON",
        processData : false,
        contentType:false,
        
        success: function(response) {

            if (response.errors) {
                var errorMsg = '';
                $.each(response.errors, function(field, errors) {
                    $.each(errors, function(index, error) {
                        errorMsg += error + '<br>';
                    });
                });
                iziToast.error({
                    message: errorMsg,
                    position: 'topRight'
                });
                
            } else {
            iziToast.success({
            message: response.success,
            position: 'topRight'
            
                    });
            }
                    
        },
        error: function(xhr, status, error) {
        
            iziToast.error({
                message: 'An error occurred: ' + error,
                position: 'topRight'
            });
        }
    
        });
    
